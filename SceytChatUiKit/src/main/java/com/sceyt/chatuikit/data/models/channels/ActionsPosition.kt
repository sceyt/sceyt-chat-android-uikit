package com.sceyt.chatuikit.data.models.channels

/**
 * Position where an input action should be displayed.
 */
enum class ActionsPosition {
    /**
     * Actions displayed on the left side (start) of the input field
     */
    LEADING,
    
    /**
     * Actions displayed on the right side (end) of the input field
     */
    TRAILING
}

